                    <div class="des">
                      <pre>
Rack and pinion steering employs a rack (a linear gear) meshing with a pinion gear connected
to the steering wheel, translating rotational motion into linear motion to steer the wheels directly. 
Recirculating ball steering utilizes a worm gear inside a steering box, where the rotation of
the steering wheel moves a series of recirculating ball bearings, transferring motion to a sector
gear connected to the wheels, resulting in steering. 
Steer-by-wire, an advanced system, relies on electronic 
sensors to detect steering wheel input, 
transmitting them to an electronic control unit
(ECU),which communicates with actuators to turn 
the wheels accordingly.
